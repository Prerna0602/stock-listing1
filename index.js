// sorting prices low to high
function sortPrices(price1, price2) {
  return price1.price - price2.price;
}
app.get('/stocks/sort/pricing', (req, res) => {
  stocks.sort(sortPrices);
  res.json({ stocks: stocks });
});

// sorting based on growth low to high
function sortGrowth(growth1, growth2) {
  return growth1.growth - growth2.growth;
}
app.get('/stocks/sort/growth', (req, res) => {
  stocks.sort(sortGrowth);
  res.json({ stocks: stocks });
});

// Filter the stocks based on the 2 Stock Exchange (NSE. and BSE)

function stockExchangeFiltering(stock, exchange1) {
  return stock.exchange.toLowerCase() === exchange1.toLowerCase();
}
app.get('/stocks/filter/exchange/:exchange1', (req, res) => {
  let exchange1 = req.params.exchange1;
  let stockExchangeFilter = stocks.filter((stock) =>
    stockExchangeFiltering(stock, exchange1)
  );
  res.json({ stocks: stockExchangeFilter });
});

// Filter the stocks based on the Industrial Sector.

function industrialSectorFiltering(stock, industry1) {
  return stock.industry.toLowerCase() === industry1.toLowerCase();
}
app.get('/stocks/filter/industry/:industry1', (req, res) => {
  let industry1 = req.params.industry1;
  let industrialSectorFilter = stocks.filter((stock) =>
    industrialSectorFiltering(stock, industry1)
  );
  res.json({ stocks: industrialSectorFilter });
});

